package sinais;

public class calculadora {
double numero1;
double numero2;
double resultado;

	void calcularSoma(){
		resultado=numero1 + numero2;
	}
	
	void calcularSub(){
		resultado=numero1 - numero2;
	}
	
	void calcularDiv(){
		resultado=numero1/numero2;
	}
	
	void calcularMult(){
		resultado=numero1 * numero2;
	}

}
